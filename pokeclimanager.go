func pokePokeDecoder(url string, c *pokecache.Cache) (fullPokemon, error) {
	var who fullPokemon
	//code to handle url being in cache already
	value, ok := c.Get(url)
	if ok {
		fmt.Println("-------Cached data used--------")
		err := json.Unmarshal(value, &who)
		if err != nil {
			return who, fmt.Errorf("Error: decoding of cached bytes failed\n")
		}
		return who, nil
	}
	//cache logic end
	res, err := http.Get(url)
	if err != nil {
		return who, fmt.Errorf("Error: HTTPS request to pokeAPI failed\n")
	}
	defer res.Body.Close()
	pokeBytes, err := io.ReadAll(res.Body)
	if err != nil {
		return who, fmt.Errorf("Error: HTTP response did not decode successfully\n")
	}
	//decode successful, proceed to cache bytes
	c.Add(url, pokeBytes)
	err = json.Unmarshal(pokeBytes, &who)
	if err != nil {
		return who, fmt.Errorf("Error: response bytes did not unmarshal successfully\n")
	}

	return who, nil
}